import { z } from "zod";

/* =====================================================================
 * Users
 * Note: auth is simulated. A single "current user" row is bootstrapped
 * on first run. `role` controls credit behavior + admin UI.
 * ===================================================================== */
export const users = sqliteTable("users", {
  id: integer("id").primaryKey({ autoIncrement: true }),
  full_name: text("full_name").notNull(),
  email: text("email").notNull().unique(),
  role: text("role").notNull().default("user"), // 'user' | 'admin'
  credits: integer("credits").notNull().default(0),
  created_date: text("created_date").notNull(), // ISO string
  // Server-side scrypt password hash (format: "scrypt$N$r$p$<saltHex>$<keyHex>").
  // Nullable so legacy / seeded rows can exist before the owner sets a password.
  // A user with a null hash must complete the first-time password setup flow
  // (POST /api/me/set-password) before they can sign in.
  password_hash: text("password_hash"),
});

export const insertUserSchema = createInsertSchema(users).omit({ id: true });
export type InsertUser = z.infer<typeof insertUserSchema>;
export type User = typeof users.$inferSelect;

