/* =====================================================================
 * Analyses
 * result_text is markdown; automation_risk is "High" | "Medium" | "Low"
 * provider_used identifies the model/source (e.g. "MockInvokeLLM")
 * ===================================================================== */
export const analyses = sqliteTable("analyses", {
  id: integer("id").primaryKey({ autoIncrement: true }),
  created_date: text("created_date").notNull(),
  created_by: integer("created_by").notNull(),
  job_title: text("job_title").notNull(),
  job_description: text("job_description").notNull(),
  technology_context: text("technology_context"),
  resume_id: integer("resume_id"),
  result_text: text("result_text").notNull(),
  provider_used: text("provider_used").notNull(),
  automation_risk: text("automation_risk").notNull(), // High | Medium | Low
  risk_score: integer("risk_score").notNull(), // 1-100
  // Soft-lock flag: when a user with 0 credits runs an analysis the
  // record is created with is_locked=1. The frontend reveals the
  // headers / score gauge / banner but obfuscates the readable body
  // until the user buys credits or unlocks the report.
  is_locked: integer("is_locked", { mode: "boolean" }).notNull().default(false),
});

export const insertAnalysisSchema = createInsertSchema(analyses).omit({ id: true });
export type InsertAnalysis = z.infer<typeof insertAnalysisSchema>;
export type Analysis = typeof analyses.$inferSelect;

